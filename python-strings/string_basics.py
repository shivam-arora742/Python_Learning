# In python,strings are immutable data types.
# declared strings as double quotes("") or single quotes('').
# single characters are also strings in python with length =1.

# Declaration:

# single line string:
name="shivam arora"
age='20'
print(name +" "+age)

# multi-line strings:
school="""abc
def"""
print(school)

# Indexing in strings:

# indexes are specific location of a character in string.
name="shivam"
print(name[2])
print(name[4])
# indexing starts from 0 to length-1.
